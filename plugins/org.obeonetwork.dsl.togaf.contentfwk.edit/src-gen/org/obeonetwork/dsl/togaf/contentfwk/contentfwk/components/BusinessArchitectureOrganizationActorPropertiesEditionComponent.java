public class BusinessArchitectureOrganizationActorPropertiesEditionComponent extends SinglePartPropertiesEditingComponent {

	
	public static String ORGANIZATIONACTOR_PART = "OrganizationActor"; //$NON-NLS-1$

	
	/**
	 * Settings for Actors ReferencesTable
	 */
	protected ReferencesTableSettings actorsSettings;
	
	/**
	 * Settings for Organizations ReferencesTable
	 */
	protected ReferencesTableSettings organizationsSettings;
	
	
	/**
	 * Default constructor
	 * 
	 */
	public BusinessArchitectureOrganizationActorPropertiesEditionComponent(PropertiesEditingContext editingContext, EObject businessArchitecture, String editing_mode) {
		super(editingContext, businessArchitecture, editing_mode);
		parts = new String[] { ORGANIZATIONACTOR_PART };
		repositoryKey = ContentfwkViewsRepository.class;
		partKey = ContentfwkViewsRepository.OrganizationActor.class;
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.emf.eef.runtime.api.component.IPropertiesEditionComponent#initPart(java.lang.Object, int, org.eclipse.emf.ecore.EObject, 
	 *      org.eclipse.emf.ecore.resource.ResourceSet)
	 * 
	 */
	public void initPart(Object key, int kind, EObject elt, ResourceSet allResource) {
		setInitializing(true);
		if (editingPart != null && key == partKey) {
			editingPart.setContext(elt, allResource);
			final BusinessArchitecture businessArchitecture = (BusinessArchitecture)elt;
			final OrganizationActorPropertiesEditionPart organizationActorPart = (OrganizationActorPropertiesEditionPart)editingPart;
			// init values
			if (isAccessible(ContentfwkViewsRepository.OrganizationActor.Organizationactors.actors)) {
				actorsSettings = new ReferencesTableSettings(businessArchitecture, ContentfwkPackage.eINSTANCE.getBusinessArchitecture_Actors());
				organizationActorPart.initActors(actorsSettings);
			}
			if (isAccessible(ContentfwkViewsRepository.OrganizationActor.Organizationactors.organizations)) {
				organizationsSettings = new ReferencesTableSettings(businessArchitecture, ContentfwkPackage.eINSTANCE.getBusinessArchitecture_Units());
				organizationActorPart.initOrganizations(organizationsSettings);
			}
			// init filters
			organizationActorPart.addFilterToActors(new ViewerFilter() {
			
					/**
					 * {@inheritDoc}
					 * 
					 * @see org.eclipse.jface.viewers.ViewerFilter#select(org.eclipse.jface.viewers.Viewer, java.lang.Object, java.lang.Object)
					 */
					public boolean select(Viewer viewer, Object parentElement, Object element) {
						return (element instanceof String && element.equals("")) || (element instanceof Actor); //$NON-NLS-1$ 
					}
			
			});
			// Start of user code for additional businessfilters for Actors
			
			// End of user code
			
			organizationActorPart.addFilterToOrganizations(new ViewerFilter() {
			
					/**
					 * {@inheritDoc}
					 * 
					 * @see org.eclipse.jface.viewers.ViewerFilter#select(org.eclipse.jface.viewers.Viewer, java.lang.Object, java.lang.Object)
					 */
					public boolean select(Viewer viewer, Object parentElement, Object element) {
						return (element instanceof String && element.equals("")) || (element instanceof OrganizationUnit); //$NON-NLS-1$ 
					}
			
			});
			// Start of user code for additional businessfilters for Organizations
			
			// End of user code
			
			// init values for referenced views
			
			// init filters for referenced views
			
		}
		setInitializing(false);
	}





	/**
	 * {@inheritDoc}
	 * @see org.eclipse.emf.eef.runtime.impl.components.StandardPropertiesEditionComponent#associatedFeature(java.lang.Object)
	 */
	public EStructuralFeature associatedFeature(Object editorKey) {
		if (editorKey == ContentfwkViewsRepository.OrganizationActor.Organizationactors.actors) {
			return ContentfwkPackage.eINSTANCE.getBusinessArchitecture_Actors();
		}
		if (editorKey == ContentfwkViewsRepository.OrganizationActor.Organizationactors.organizations) {
			return ContentfwkPackage.eINSTANCE.getBusinessArchitecture_Units();
		}
		return super.associatedFeature(editorKey);
	}

	/**
	 * {@inheritDoc}
	 * @see org.eclipse.emf.eef.runtime.impl.components.StandardPropertiesEditionComponent#updateSemanticModel(org.eclipse.emf.eef.runtime.api.notify.IPropertiesEditionEvent)
	 * 
	 */
	public void updateSemanticModel(final IPropertiesEditionEvent event) {
		BusinessArchitecture businessArchitecture = (BusinessArchitecture)semanticObject;
		if (ContentfwkViewsRepository.OrganizationActor.Organizationactors.actors == event.getAffectedEditor()) {
			if (event.getKind() == PropertiesEditionEvent.ADD) {
				EReferencePropertiesEditionContext context = new EReferencePropertiesEditionContext(editingContext, this, actorsSettings, editingContext.getAdapterFactory());
				PropertiesEditingProvider provider = (PropertiesEditingProvider)editingContext.getAdapterFactory().adapt(semanticObject, PropertiesEditingProvider.class);
				if (provider != null) {
					PropertiesEditingPolicy policy = provider.getPolicy(context);
					if (policy instanceof CreateEditingPolicy) {
						policy.execute();
					}
				}
			} else if (event.getKind() == PropertiesEditionEvent.EDIT) {
				EObjectPropertiesEditionContext context = new EObjectPropertiesEditionContext(editingContext, this, (EObject) event.getNewValue(), editingContext.getAdapterFactory());
				PropertiesEditingProvider provider = (PropertiesEditingProvider)editingContext.getAdapterFactory().adapt((EObject) event.getNewValue(), PropertiesEditingProvider.class);
				if (provider != null) {
					PropertiesEditingPolicy editionPolicy = provider.getPolicy(context);
					if (editionPolicy != null) {
						editionPolicy.execute();
					}
				}
			} else if (event.getKind() == PropertiesEditionEvent.REMOVE) {
				actorsSettings.removeFromReference((EObject) event.getNewValue());
			} else if (event.getKind() == PropertiesEditionEvent.MOVE) {
				actorsSettings.move(event.getNewIndex(), (Actor) event.getNewValue());
			}
		}
		if (ContentfwkViewsRepository.OrganizationActor.Organizationactors.organizations == event.getAffectedEditor()) {
			if (event.getKind() == PropertiesEditionEvent.ADD) {
				EReferencePropertiesEditionContext context = new EReferencePropertiesEditionContext(editingContext, this, organizationsSettings, editingContext.getAdapterFactory());
				PropertiesEditingProvider provider = (PropertiesEditingProvider)editingContext.getAdapterFactory().adapt(semanticObject, PropertiesEditingProvider.class);
				if (provider != null) {
					PropertiesEditingPolicy policy = provider.getPolicy(context);
					if (policy instanceof CreateEditingPolicy) {
						policy.execute();
					}
				}
			} else if (event.getKind() == PropertiesEditionEvent.EDIT) {
				EObjectPropertiesEditionContext context = new EObjectPropertiesEditionContext(editingContext, this, (EObject) event.getNewValue(), editingContext.getAdapterFactory());
				PropertiesEditingProvider provider = (PropertiesEditingProvider)editingContext.getAdapterFactory().adapt((EObject) event.getNewValue(), PropertiesEditingProvider.class);
				if (provider != null) {
					PropertiesEditingPolicy editionPolicy = provider.getPolicy(context);
					if (editionPolicy != null) {
						editionPolicy.execute();
					}
				}
			} else if (event.getKind() == PropertiesEditionEvent.REMOVE) {
				organizationsSettings.removeFromReference((EObject) event.getNewValue());
			} else if (event.getKind() == PropertiesEditionEvent.MOVE) {
				organizationsSettings.move(event.getNewIndex(), (OrganizationUnit) event.getNewValue());
			}
		}
	}

	/**
	 * {@inheritDoc}
	 * @see org.eclipse.emf.eef.runtime.impl.components.StandardPropertiesEditionComponent#updatePart(org.eclipse.emf.common.notify.Notification)
	 */
	public void updatePart(Notification msg) {
		if (editingPart.isVisible()) {	
			OrganizationActorPropertiesEditionPart organizationActorPart = (OrganizationActorPropertiesEditionPart)editingPart;
			if (ContentfwkPackage.eINSTANCE.getBusinessArchitecture_Actors().equals(msg.getFeature()) && isAccessible(ContentfwkViewsRepository.OrganizationActor.Organizationactors.actors))
				organizationActorPart.updateActors();
			if (ContentfwkPackage.eINSTANCE.getBusinessArchitecture_Units().equals(msg.getFeature()) && isAccessible(ContentfwkViewsRepository.OrganizationActor.Organizationactors.organizations))
				organizationActorPart.updateOrganizations();
			
		}
	}


	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.emf.eef.runtime.api.component.IPropertiesEditionComponent#validateValue(org.eclipse.emf.eef.runtime.api.notify.IPropertiesEditionEvent)
	 * 
	 */
	public Diagnostic validateValue(IPropertiesEditionEvent event) {
		Diagnostic ret = Diagnostic.OK_INSTANCE;
		if (event.getNewValue() != null) {
			try {
			} catch (IllegalArgumentException iae) {
				ret = BasicDiagnostic.toDiagnostic(iae);
			} catch (WrappedException we) {
				ret = BasicDiagnostic.toDiagnostic(we);
			}
		}
		return ret;
	}

}
