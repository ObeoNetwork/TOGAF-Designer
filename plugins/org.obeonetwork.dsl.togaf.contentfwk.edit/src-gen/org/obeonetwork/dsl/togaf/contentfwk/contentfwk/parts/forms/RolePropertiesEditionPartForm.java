	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.emf.eef.runtime.api.parts.IFormPropertiesEditionPart#
	 *  createFigure(org.eclipse.swt.widgets.Composite, org.eclipse.ui.forms.widgets.FormToolkit)
	 * 
	 */
	public Composite createFigure(final Composite parent, final FormToolkit widgetFactory) {
		ScrolledForm scrolledForm = widgetFactory.createScrolledForm(parent);
		Form form = scrolledForm.getForm();
		view = form.getBody();
		GridLayout layout = new GridLayout();
		layout.numColumns = 3;
		view.setLayout(layout);
		createControls(widgetFactory, view);
		return scrolledForm;
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see org.eclipse.emf.eef.runtime.api.parts.IFormPropertiesEditionPart#
	 *  createControls(org.eclipse.ui.forms.widgets.FormToolkit, org.eclipse.swt.widgets.Composite)
	 * 
	 */
	public void createControls(final FormToolkit widgetFactory, Composite view) {
		CompositionSequence roleStep = new BindingCompositionSequence(propertiesEditionComponent);
		CompositionStep attributesStep = roleStep.addStep(ContentfwkViewsRepository.Role.Attributes.class);
		attributesStep.addStep(ContentfwkViewsRepository.Role.Attributes.name);
		attributesStep.addStep(ContentfwkViewsRepository.Role.Attributes.iD);
		attributesStep.addStep(ContentfwkViewsRepository.Role.Attributes.description);
		attributesStep.addStep(ContentfwkViewsRepository.Role.Attributes.category);
		attributesStep.addStep(ContentfwkViewsRepository.Role.Attributes.source);
		attributesStep.addStep(ContentfwkViewsRepository.Role.Attributes.owner);
		attributesStep.addStep(ContentfwkViewsRepository.Role.Attributes.estimatedFTEs);
		
		CompositionStep relatedElementsStep = roleStep.addStep(ContentfwkViewsRepository.Role.RelatedElements.class);
		relatedElementsStep.addStep(ContentfwkViewsRepository.Role.RelatedElements.decomposesRole);
		relatedElementsStep.addStep(ContentfwkViewsRepository.Role.RelatedElements.isAssumedByActors);
		relatedElementsStep.addStep(ContentfwkViewsRepository.Role.RelatedElements.accessesFunctions);
		relatedElementsStep.addStep(ContentfwkViewsRepository.Role.RelatedElements.delegates);
		relatedElementsStep.addStep(ContentfwkViewsRepository.Role.RelatedElements.isDelegatedBy);
		
		
		composer = new PartComposer(roleStep) {

			@Override
			public Composite addToPart(Composite parent, Object key) {
				if (key == ContentfwkViewsRepository.Role.Attributes.class) {
					return createAttributesGroup(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.Attributes.name) {
					return 		createNameText(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.Attributes.iD) {
					return 		createIDText(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.Attributes.description) {
					return 		createDescriptionText(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.Attributes.category) {
					return 		createCategoryText(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.Attributes.source) {
					return 		createSourceText(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.Attributes.owner) {
					return 		createOwnerText(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.Attributes.estimatedFTEs) {
					return 		createEstimatedFTEsText(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.RelatedElements.class) {
					return createRelatedElementsGroup(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.RelatedElements.decomposesRole) {
					return createDecomposesRoleFlatComboViewer(parent, widgetFactory);
				}
				if (key == ContentfwkViewsRepository.Role.RelatedElements.isAssumedByActors) {
					return createIsAssumedByActorsReferencesTable(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.RelatedElements.accessesFunctions) {
					return createAccessesFunctionsReferencesTable(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.RelatedElements.delegates) {
					return createDelegatesReferencesTable(widgetFactory, parent);
				}
				if (key == ContentfwkViewsRepository.Role.RelatedElements.isDelegatedBy) {
					return createIsDelegatedByReferencesTable(widgetFactory, parent);
				}
				return parent;
			}
		};
		composer.compose(view);
	}
